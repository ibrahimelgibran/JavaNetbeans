public class operatorAssignment {
    public static void main(String[] args) {
        /*
        Operator Penugasan
            =
            +=
            -=
            *=
            /=
            %=
        */
        
        int a, b;
        a = 5;
        b = 2;
        
        a -= b;
        System.out.println(a);
        a += b;
        System.out.println(a);
        a *= b;
        System.out.println(a + "\n");
        
        double c, d;
        c = 5;
        d = 2;
        
        c /= d;
        System.out.println(c);
        c %= d;
        System.out.println(c);
       
   
        
        
        
     
    }
}
