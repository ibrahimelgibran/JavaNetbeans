public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        // integer (int)
        int umur = 20;
        System.out.println("Umur saya sekarang " + umur + " tahun");
        
        // floadting point (float)
        float berat = (float) 12;
        System.out.println("Berat " + berat + " kg");
        
        // double floating point (double)
        double panjang = 10.199999;
        System.out.println("panjang " + panjang + " m");
        
        
        // text (String)
        String nama = "Gibran";
        System.out.println("nama saya " + nama);
        
        // character (char)
        char gender = 'L';
        System.out.println("Gender = " + gender);
        
        // Boolean (bool)
        boolean sehat = true;
        System.out.println("tubuhku " + sehat);
    }
    
}
